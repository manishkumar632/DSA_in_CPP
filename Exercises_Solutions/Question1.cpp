// Write a program that declares variables of each basic data type (int, float, double, char, and bool), assigns them values, and prints these values to the console.

#include <iostream>
int main(){
    int a = 10;
    float b = 10.5;
    char c = 'a';
    bool d = true;
    double e = 10.5324;
    std::cout << "integer a : " << a << std::endl;
    std::cout << "float b : " << b << std::endl;
    std::cout << "char c : " << c << std::endl;
    std::cout << "bool d : " << d << std::endl;
    std::cout << "double e : " << e << std::endl;
    return 0;
}